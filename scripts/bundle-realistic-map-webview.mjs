import esbuild from 'esbuild';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const mode = process.argv.includes('--forklift') ? 'forklift' : 'realistic';

const entry =
  mode === 'forklift'
    ? path.join(root, 'packages/shared/forklift-map/webviewEntry.ts')
    : path.join(root, 'packages/shared/realistic-map/webviewEntry.ts');

const outDir =
  mode === 'forklift'
    ? path.join(root, 'apps/mobile/src/components/forkliftMap3d')
    : path.join(root, 'apps/mobile/src/components/realisticMap3d');

const outJs = path.join(outDir, 'generated.runtime.js');
const outTs = path.join(outDir, 'generatedSceneScript.ts');
const globalName = mode === 'forklift' ? 'GRForkliftMapRuntime' : 'GRRealisticMapRuntime';
const exportConst = mode === 'forklift' ? 'FORKLIFT_MAP_RUNTIME_SCRIPT' : 'REALISTIC_MAP_RUNTIME_SCRIPT';

await esbuild.build({
  entryPoints: [entry],
  bundle: true,
  format: 'iife',
  globalName,
  platform: 'browser',
  target: ['es2019'],
  outfile: outJs,
  plugins: [
    {
      name: 'three-global',
      setup(build) {
        build.onResolve({ filter: /^three$/ }, () => ({
          path: 'three-global',
          namespace: 'three-global',
        }));
        build.onLoad({ filter: /.*/, namespace: 'three-global' }, () => ({
          contents: 'module.exports = THREE;',
          loader: 'js',
        }));
      },
    },
  ],
  logLevel: 'info',
});

const runtimeJs = fs.readFileSync(outJs, 'utf8');
const banner = `// Generated by scripts/bundle-realistic-map-webview.mjs (${mode}) — do not edit by hand.\n`;
const body = `${banner}export const ${exportConst} = ${JSON.stringify(runtimeJs)};\n`;
fs.writeFileSync(outTs, body);
